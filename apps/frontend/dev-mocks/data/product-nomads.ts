//
// FГѓВіrmula: MГѓВ©dia ponderada pela quantidade de entregas Гўв‚¬вЂќ apenas nГѓВґmades ATIVOS.
//
//   rating = ГЋВЈ(overallRating ГѓвЂ” overallDeliveries) / ГЋВЈ(overallDeliveries)
//
// Justificativa: nГѓВґmades com mais entregas possuem avaliaГѓВ§ГѓВµes mais consolidadas.
// Um nГѓВґmade novo com 1 entrega a 5.0 nГѓВЈo deve ter o mesmo peso que alguГѓВ©m
// com 50 entregas a 4.5, pois hГѓВЎ muito menos histГѓВіrico no primeiro caso.
//
// Fallback: se todos os ativos tГѓВЄm 0 entregas ГўвЂ вЂ™ mГѓВ©dia aritmГѓВ©tica simples.
// Produtos sem nГѓВґmades habilitados retornam count = 0 e rating = 0.
//
export function computeProductRating(productId: string): {
  rating: number;
  count: number; // nГѓВґmades ATIVOS habilitados
  totalDeliveries: number;
} {
  const nomads = mockProductNomades[productId] ?? [];
  const active = nomads.filter((n) => n.nomadeStatus === "active");

  if (active.length === 0) {
    return { rating: 0, count: 0, totalDeliveries: 0 };
  }

  const totalDeliveries = active.reduce((s, n) => s + n.overallDeliveries, 0);

  if (totalDeliveries === 0) {
    // Fallback: mГѓВ©dia aritmГѓВ©tica simples
    const avg = active.reduce((s, n) => s + n.overallRating, 0) / active.length;
    return {
      rating: Math.round(avg * 10) / 10,
      count: active.length,
      totalDeliveries: 0,
    };
  }

  const weightedSum = active.reduce(
    (s, n) => s + n.overallRating * n.overallDeliveries,
    0,
  );

  return {
    rating: Math.round((weightedSum / totalDeliveries) * 10) / 10,
    count: active.length,
    totalDeliveries,
  };
}
